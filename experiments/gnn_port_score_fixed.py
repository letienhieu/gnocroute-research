#!/usr/bin/env python3
"""
Generate fixed GNN Port Score routing headers with deadlock-free VC management.
Uses adaptive_xy_yx style: GNN mode preference table decides XY vs YX at injection,
mode is sticky via VCs for deadlock avoidance.
"""
import numpy as np, os, time

G = 8
N = G * G
EXP_DIR = '/home/opc/.openclaw/workspace/papers/paper03-q1-jsa/experiments'

def get_minimal_ports(cur, dst):
    cx, cy = cur % G, cur // G; dx, dy = dst % G, dst // G
    ports = []
    if dx > cx: ports.append(0)
    if dx < cx: ports.append(1)
    if dy > cy: ports.append(2)
    if dy < cy: ports.append(3)
    return ports

def generate_header(scores, name, version_label, output_path):
    # Precompute mode preference: 0=XY(E/W first), 1=YX(N/S first)
    mode_prefs = np.zeros((N, N), dtype=np.int8)
    for cur in range(N):
        for dst in range(N):
            if cur == dst: continue
            mp = get_minimal_ports(cur, dst)
            
            # XY mode: E/W first, then N/S
            xy_port = -1
            for p in [0, 1]:
                if p in mp: xy_port = p; break
            if xy_port < 0:
                for p in [2, 3]:
                    if p in mp: xy_port = p; break
            
            # YX mode: N/S first, then E/W
            yx_port = -1
            for p in [2, 3]:
                if p in mp: yx_port = p; break
            if yx_port < 0:
                for p in [0, 1]:
                    if p in mp: yx_port = p; break
            
            if xy_port >= 0 and yx_port >= 0:
                xy_score = scores[cur, dst, xy_port]
                yx_score = scores[cur, dst, yx_port]
                mode_prefs[cur, dst] = 1 if yx_score > xy_score else 0
            elif xy_port >= 0:
                mode_prefs[cur, dst] = 0
            elif yx_port >= 0:
                mode_prefs[cur, dst] = 1
            else:
                mode_prefs[cur, dst] = 0  # Shouldn't happen
    
    lines = []
    lines.append(f"// Auto-generated by gnn_port_score_fixed.py")
    lines.append(f"// Mesh 8x8, generated {time.strftime('%Y-%m-%d %H:%M:%S')}")
    lines.append(f"// {version_label}")
    lines.append("// Port mapping: 0=E(x+1), 1=W(x-1), 2=S(y+1), 3=N(y-1)")
    lines.append("// Deadlock-free: sticky VC mode (like adaptive_xy_yx)")
    lines.append(f"#ifndef _GNN_PORT_SCORE_ROUTE_8X8_{name}_H_")
    lines.append(f"#define _GNN_PORT_SCORE_ROUTE_8X8_{name}_H_")
    lines.append('#include "booksim.hpp"')
    lines.append('#include "routefunc.hpp"')
    lines.append('#include "kncube.hpp"')
    lines.append("")
    lines.append("extern int dor_next_mesh(int,int,bool);")
    lines.append("extern int gNumVCs;")
    lines.append("")
    
    # Mode preference table (64x64, int8 packed)
    lines.append(f"static const char gnn_port_score_mode_8x8_{name}[{N}][{N}]={{")
    for cur in range(N):
        row = "  {" + ",".join(str(int(mode_prefs[cur, dst])) for dst in range(N)) + "}"
        if cur < N - 1: row += ","
        lines.append(row)
    lines.append("};")
    lines.append("")
    
    # Score table (keep for reference, not used in routing)
    lines.append(f"static const float gnn_port_scores_8x8_{name}[{N}][{N}][4]={{")
    for cur in range(N):
        lines.append(f"  {{  // cur={cur}")
        for dst in range(N):
            row = "    {" + ",".join(f"{scores[cur, dst, p]:.6f}f" for p in range(4)) + "}"
            if cur < N - 1 or dst < N - 1: row += ","
            lines.append(row)
        if cur < N - 1: lines.append("  },")
        else: lines.append("  }")
    lines.append("};")
    lines.append("")
    
    routing = f"""
void gnn_port_score_route_8x8_{name}_mesh(const Router*r, const Flit*f,
int in_channel, OutputSet*outputs, bool inject){{
  int vcBegin=0, vcEnd=gNumVCs-1;
  if (f->type == Flit::READ_REQUEST)   {{vcBegin=gReadReqBeginVC;  vcEnd=gReadReqEndVC;}}
  if (f->type == Flit::WRITE_REQUEST)  {{vcBegin=gWriteReqBeginVC; vcEnd=gWriteReqEndVC;}}
  if (f->type == Flit::READ_REPLY)     {{vcBegin=gReadReplyBeginVC;vcEnd=gReadReplyEndVC;}}
  if (f->type == Flit::WRITE_REPLY)    {{vcBegin=gWriteReplyBeginVC;vcEnd=gWriteReplyEndVC;}}
  assert(((f->vc>=vcBegin)&&(f->vc<=vcEnd))||(inject&&(f->vc<0)));
  (void)in_channel;
  int out_port;
  if(inject){{out_port=-1;outputs->Clear();outputs->AddRange(out_port,vcBegin,vcEnd);return;}}
  if(r->GetID()==f->dest){{out_port=2*gN;outputs->Clear();outputs->AddRange(out_port,vcBegin,vcEnd);return;}}
  
  int cur=r->GetID();
  int dest=f->dest;
  
  // Each message class needs >=2 VCs for deadlock-free adaptive routing
  int const available_vcs = (vcEnd - vcBegin + 1) / 2;
  assert(available_vcs > 0);
  
  // Use GNN mode preference at injection; keep mode sticky via VCs in-network
  bool prefer_yx;
  if(in_channel < 2*gN) {{
    // In-network: mode determined by VC class
    prefer_yx = (f->vc >= (vcBegin + available_vcs));
  }} else {{
    // Injection: use precomputed GNN mode preference
    prefer_yx = (gnn_port_score_mode_8x8_{name}[cur][dest] != 0);
  }}
  
  // XY or YX route
  if (!prefer_yx) {{
    out_port = dor_next_mesh(cur, dest, false);
    vcEnd = vcBegin + available_vcs - 1;
  }} else {{
    out_port = dor_next_mesh(cur, dest, true);
    vcBegin = vcBegin + available_vcs;
  }}
  
  outputs->Clear();
  outputs->AddRange(out_port, vcBegin, vcEnd);
}}
"""
    lines.append(routing.strip())
    lines.append(f"#endif /* _GNN_PORT_SCORE_ROUTE_8X8_{name}_H_ */")
    
    with open(output_path, 'w') as f:
        f.write('\n'.join(lines) + '\n')
    
    sz = os.path.getsize(output_path)
    xy = int((mode_prefs == 0).sum()) - N
    yx = int((mode_prefs == 1).sum())
    print(f"[Header] {output_path} ({sz/1024:.1f} KB)")
    print(f"  XY-mode pairs: {xy} ({xy/(N*N-N)*100:.1f}%)")
    print(f"  YX-mode pairs: {yx} ({yx/(N*N-N)*100:.1f}%)")


if __name__ == '__main__':
    out = '/home/opc/.openclaw/workspace/booksim2/src'
    
    print("="*60)
    print("Gen fixed v4 header (zero-shot)")
    generate_header(np.load(f'{EXP_DIR}/gnn_port_scores_8x8_v4.npy'), 'v4',
                    'GNN v4 (zero-shot 4x4->8x8)', f'{out}/gnn_port_score_route_8x8_v4.h')
    
    print("\n"+"="*60)
    print("Gen fixed v5 header (fine-tuned)")
    generate_header(np.load(f'{EXP_DIR}/gnn_port_scores_8x8_v5b.npy'), 'v5',
                    'GNN v5b (fine-tuned on 8x8)', f'{out}/gnn_port_score_route_8x8_v5.h')
    
    print("\nDone. Recompile BookSim2.")
